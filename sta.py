my_stack = []
my_stack.append(1)  # Thêm phần tử 1 vào Stack
print("\nIn ra phần tử đã được thêm vào stack:", my_stack)
my_stack.append(2)  # Thêm phần tử 2 vào Stack
print("In ra phần đã được thêm vào stack:", my_stack)
my_stack.pop()  # Lấy phần tử cuối cùng ra khỏi Stack (2)
print("Phần tử được lấy ra khỏi stack: ", my_stack)
my_stack.peek()
print(my_stack)


